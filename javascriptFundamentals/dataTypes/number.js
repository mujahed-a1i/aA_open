// The Number data type in JavaScript does exactly what you expect! 
// It is used to represent any numerical value, including integers and decimal numbers.

/* basic arithmetic operators (order of operations)
* multiplication
/ division
% modulo
+ addition
- subtraction
*/

console.log (2 + 3); // ans: 5
console.log(42 - 42); // ans: 5
console.log(-4 * 1.5); // ans: -6
console.log(25 / 8); // ans: 3.125

console.log(5 * 3 * 2); // ans: 30
console.log(2 + 3 * 5); // ans: 17
console.log((2 + 3) * 5); // ans:` 25

/*
What you've learned
The Number type is used to represent integer and decimal values
The operators +, -, /, * perform the normal math operations of addition, subtraction, division, and multiplication respectively
a % b returns the remainder when we divide a by b; we call this operation modulo
JavaScript follows the usual mathematical order of operations and we can use the () to force precedence
*/
